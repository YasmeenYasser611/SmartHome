#ifndef STD_TYPES_H
#define STD_TYPES_H

typedef unsigned char               u8;          //unsigned char is 1 byte =8bit
typedef signed char                 s8;          //signed char 1 byte =8bit
typedef unsigned short int          u16;       //unsigned short int 2byte = 16bit
typedef signed short int            s16;     //signed short int 2byte = 16bit
typedef unsigned long int           u32;      //unsigned int (long) 4bytes = 32bit
typedef signed long int             s32;   //signed int (long) 4bytes = 32bit
typedef float                       f32;  // float is 4bytes = 32bit
typedef unsigned long long int      u64;
typedef signed long long int        s64;
typedef double                      f64; //double is 8bytes = 64bit
typedef long double                 f128;


#endif

